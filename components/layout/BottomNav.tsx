const NAV_ITEMS = [
  { href: '/home', label: '홈', Icon: HomeIcon },
  { href: '/input', label: '입력', Icon: PlusIcon },
  { href: '/library', label: '라이브러리', Icon: BookIcon },
  { href: '/actions', label: '실행', Icon: CheckIcon },
  { href: '/mypage', label: '마이', Icon: UserIcon },
]

export default function BottomNav() {
  const pathname = usePathname()

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 bg-white border-t border-gray-100 safe-area-inset-bottom">
      <div className="flex max-w-lg mx-auto">
        {NAV_ITEMS.map(({ href, label, Icon }) => {
          const isActive = pathname.startsWith(href)
          return (
            <Link
              key={href}
              href={href}
              className={`flex-1 flex flex-col items-center justify-center py-3 gap-0.5 transition-colors ${
                isActive ? 'text-black' : 'text-gray-400'
              }`}
            >
              <Icon active={isActive} />
              <span className={`text-[10px] font-medium ${isActive ? 'text-black' : 'text-gray-400'}`}>
                {label}
              </span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
